from sudoku_helper import SudokuPuzzle


def test_puzzle_1():
    puzzle = [
        [0, 9, 3, 8, 4, 6, 0, 1, 0],
        [0, 2, 0, 9, 3, 0, 0, 7, 4],
        [0, 5, 8, 1, 7, 0, 3, 9, 0],
        [0, 0, 0, 0, 0, 4, 7, 0, 1],
        [2, 0, 7, 0, 0, 1, 5, 0, 0],
        [6, 0, 5, 0, 0, 8, 0, 0, 0],
        [8, 0, 0, 2, 6, 0, 0, 0, 9],
        [0, 0, 0, 0, 1, 0, 0, 8, 0],
        [9, 0, 1, 0, 0, 0, 0, 3, 2],
    ]
    answer = [
        [7, 9, 3, 8, 4, 6, 2, 1, 5],
        [1, 2, 6, 9, 3, 5, 8, 7, 4],
        [4, 5, 8, 1, 7, 2, 3, 9, 6],
        [3, 8, 9, 6, 5, 4, 7, 2, 1],
        [2, 4, 7, 3, 9, 1, 5, 6, 8],
        [6, 1, 5, 7, 2, 8, 9, 4, 3],
        [8, 7, 4, 2, 6, 3, 1, 5, 9],
        [5, 3, 2, 4, 1, 9, 6, 8, 7],
        [9, 6, 1, 5, 8, 7, 4, 3, 2],
    ]

    sudoku = SudokuPuzzle(puzzle)
    for _ in range(6):
        sudoku.iterate_solving()

    assert sudoku.grid == answer


